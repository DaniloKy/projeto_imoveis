package com.example.projeto_imoveis.classes;

public class User {

    private int id;
    public String user;
    private String password;

    public User(){}

    public User(String uer){
        this.user = user;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
